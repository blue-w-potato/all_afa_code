# 阿華對2及3很有興趣，每次看到一個整數，都會將這個整數z拆成 
# z = 2^a*3^b*c {0<=a,b<=20} {1<=z<=2*10^9}
# 給一個正整數，請幫忙找出 a,b,c
# 範例1、2如下
# 5995296 = 2^5 *3^6 * 257
# 123456789 = 2^0 * 3^2 * 13717421

two = 0
three = 0
a = int(input())

while a%2==0:
    a//=2
    two +=1
while a%3==0:
    a//=3
    three +=1
    
print(two, three, a)